
public class Activity {
	
	String activityID;
	String activityDate;
	String activityTime;
	String activityDetails;
	String itemName;
	
	public String setactivityID() {
		return activityID;
	}
	public void getactivityID(String activityID) {
		this.activityID = activityID;
	}
	public String setactivityDate() {
		return activityDate;
	}
	public void getactivityDate(String activityDate) {
		this.activityDate = activityDate;
	}
	public String setactivityTime() {
		return activityTime;
	}
	public void getactivityTime(String activityTime) {
		this.activityTime = activityTime;
	}
	public String setactivityDetails() {
		return activityDetails;
	}
	public void getactivityDetails(String activityDetails) {
		this.activityDetails = activityDetails;
	}
	public String setitemName() {
		return itemName;
	}
	public void getitemName(String itemName) {
		this.itemName = itemName;
	}
}
