
public class khairatstaff {
	
	String staffID;
	String staffName;
	int staffContactNo;
	String staffEmail;
	String staffPassword;
	String staffAddress;
	
	public String getstaffID() {
		return staffID;
	}
	public void  setstaffID(String staffID) {
		this.staffID = staffID;
	}
	public String getstaffName() {
		return staffName;
	}
	public void getstaffName(String staffName) {
		this.staffName = staffName;
	}
	public int setstaffContactNo() {
		return staffContactNo;
	}
	public void getstaffContactNo(int staffContactNo) {
		this.staffContactNo = staffContactNo;
	}
	public String setstaffEmail() {
		return staffEmail;
	}
	public void getstaffEmail(String staffEmail) {
		this.staffEmail = staffEmail;
	}
	public String setstaffPassword() {
		return staffPassword;
	}
	public void getstaffPassword(String staffPassword) {
		this.staffPassword = staffPassword;
	}
	public String setstaffAddress() {
		return staffAddress;
	}
	public void getstaffAddress(String staffAddress) {
		this.staffAddress = staffAddress;
	}
}
